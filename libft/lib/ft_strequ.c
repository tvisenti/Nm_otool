#include "../inc/libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int	i;

	if (!s1 || !s2)
		return (0);
	i = 0;
	while (*s1++ && *s2++)
	{
		if (*s1 != *s2)
			return (0);
	}
	return (1);
}
